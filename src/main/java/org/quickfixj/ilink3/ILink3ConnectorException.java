
package org.quickfixj.ilink3;

public class ILink3ConnectorException extends Exception {

    public ILink3ConnectorException(String message) {
	super(message);
    }

    public ILink3ConnectorException(String message, Throwable cause) {
	super(message, cause);
    }

    public ILink3ConnectorException(Throwable cause) {
	super(cause);
    }
}
